a=10
b=5
m = a+b  # Addition         Adds values on either side of the operator.
n = a-b  # Subtraction      Subtracts right hand operand from left hand operand.
o = a*b  # Multiplication   Multiplies values on either side of the operator
p = a/b  # Division         Divides left hand operand by right hand operand
q = a%b  # Division         Divides left hand operand by right hand operand and returns remainder
r = a**b # Exponent         Performs exponential (power) calculation on operators
s = a//b # Floor Division   The division of operands where the result is the quotient in which the digits after the decimal point are removed. But if one of the operands is negative, the result is floored, i.e., rounded away from zero
print(m, n, o, p, q, r, s)